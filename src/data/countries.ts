export const getCountryByCode = (code: string): Country | undefined => {
  return COUNTRIES.find(country => country.code === code);
};

export const getCountryByName = (name: string, language: string = 'en'): Country | undefined => {
  return COUNTRIES.find(country => 
    language === 'fr' ? country.nameFr === name : country.name === name
  );
};

export const searchCountries = (query: string, language: string = 'en'): Country[] => {
  const lowerQuery = query.toLowerCase();
  return COUNTRIES.filter(country => {
    const name = language === 'fr' ? country.nameFr : country.name;
    return name.toLowerCase().includes(lowerQuery) || 
           country.code.toLowerCase().includes(lowerQuery);
  });
};
